#include<iostream>
using namespace std;
int main() {
    //Interger Datatypes
    int intergerValue = 20;
    cout<<"Stored integer value is : - " << intergerValue<<endl;   

    //Floating point Datatypes
    float floatValue = 3.14;
    cout<<"Stored float value is : - " <<floatValue<<endl;

    //Character datatypes
    char charValue = 'S';
    cout<<"Stored Character value is : - " <<charValue<<endl;


    // Boolean datatypes
    bool boolValue = true;
    cout<<"Stored boolean value is : - " <<boolValue<<endl;

    return 0;

}